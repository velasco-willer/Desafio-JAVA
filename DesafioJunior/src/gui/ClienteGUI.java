    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Instanciando classe e criando objeto
        Cliente cliente = new Cliente();
        cliente.setNome(jTextField1.getText());
        cliente.setCpf(jTextField2.getText());
        cliente.setEmail(jTextField3.getText());
        cliente.setTelefone(jTextField4.getText());
        // Verificando se os campos est√£o preenchidos
        if ((jTextField1.getText().isEmpty()) || (jTextField2.getText().isEmpty()) || (jTextField3.getText().isEmpty()) || (jTextField4.getText().isEmpty())) {
           JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        }
        else {
            // instanciando a classe UsuarioDAO do pacote dao e criando seu objeto dao
            ClienteDAO dao = new ClienteDAO();
            dao.adiciona(cliente);
            JOptionPane.showMessageDialog(null, "Usu√°rio "+jTextField1.getText()+" inserido com sucesso! ");
        }

        // apaga os dados preenchidos nos campos de texto
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

